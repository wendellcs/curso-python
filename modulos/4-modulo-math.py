import math

# Pi

print(math.pi)
print(f'{math.pi:.2f}')

# Número de Euler

print(math.e)
print(f'{math.e:.2f}')

# Arredondar números

# Para cima 

num = 10.4
print(math.ceil(num))

# Para baixo 

print(math.floor(num))

# Fatorial de um número

num2 = int(input("Digite um número:\n ----> "))

print(math.factorial(num2))

# Potência de Números

print(math.pow(5, 5))

# Raiz quadrada

print(math.sqrt(169))

# Máximo divisor comum ( MDC )

mdc = math.gcd(20 , 100)
print(mdc)

# Logaritmo ( Log )

print(math.log(10))