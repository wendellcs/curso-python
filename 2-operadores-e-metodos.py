movieName = "Até o último homem"

print(movieName.upper()) # Tudo maiúsculo

print(movieName.lower()) # Tudo minúsculo

print(movieName.capitalize()) # Primeira letra maiúscula

print(movieName.title()) # Primeira letra maiúscula

print(movieName.center(10 , '-')) # Retorna a string centralizada com caractere de preenchimento.

print(movieName.find("f")) # Retorna o index de um determinado caractere

print(movieName.count("o")) # Conta os caracteres.

print(movieName.replace("Até" , "Olá")) # Substitui um elemento por outro.

print(movieName.split("o")) # Parte a string em um determinado ponto.
